    private class ListListener1 implements ListListener<AxisChart2DComponentBundle> {

        private DefaultListModel defaultListModel;

        public ListListener1(DefaultListModel defaultListModel) {
            this.defaultListModel = defaultListModel;
        }

        public void isAdded(AxisChart2DComponentBundle t) {
            defaultListModel.addElement(ListElementManager.register(t));
        }

        public void isRemoved(AxisChart2DComponentBundle t) {
            defaultListModel.removeElementAt(ListElementManager.getIndex(t, defaultListModel));
        }
    }

    private class ListListener2 implements ListListener<AxisChart2DComponent> {

        private DefaultListModel defaultListModel;

        public ListListener2(DefaultListModel defaultListModel) {
            this.defaultListModel = defaultListModel;
        }

        public void isAdded(AxisChart2DComponent t) {
            defaultListModel.addElement(new CheckList.Element(t, t.getBundle().getLabel()));
            activate(t, defaultListModel.size() - 1);
            axisChart.repaintChartSurface();
        }

        public void isRemoved(AxisChart2DComponent t) {
            for (int fa = 0; fa < defaultListModel.size(); fa++) {
                CheckList.Element element = (CheckList.Element) defaultListModel.get(fa);
                AxisChart2DComponent comp = (AxisChart2DComponent) element.getObject();
                if (comp.equals(t)) {
                    disactivate(t, fa);
                }
            }
            defaultListModel.removeElementAt(ListElementManager.getIndex(t, defaultListModel));
            axisChart.repaintChartSurface();
        }
    }

