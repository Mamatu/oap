    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        int[] indices = jList1.getSelectedIndices();
        for (int index : indices) {
            AxisChart2DComponent chartComponent = listChartComponentBundle.get(index).newAxisChart2DComponent();
            listInstalledChartComponent.add(chartComponent);
        }
        axisChart.repaintChartSurface();
}//GEN-LAST:event_jButtonAddActionPerformed

    private void jButtonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveActionPerformed
        int[] indices = jList2.getSelectedIndices();
        for (int index : indices) {
            AxisChart2DComponent chartComponent = listInstalledChartComponent.get(index);
            listInstalledChartComponent.remove(chartComponent);
        }
        axisChart.repaintChartSurface();
}//GEN-LAST:event_jButtonRemoveActionPerformed

    private void disactivate(AxisChart2DComponent chartComponent, int index) {
        displayStackImpl.remove(chartComponent);
        ((CheckList.Element) defaultListModel1.getElementAt(index)).isAvtive = false;
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int[] indices = jList2.getSelectedIndices();
        for (int index : indices) {
            AxisChart2DComponent chartComponent = listInstalledChartComponent.get(index);
            disactivate(chartComponent, index);
        }
        jList2.repaint();
        axisChart.repaintChartSurface();
}//GEN-LAST:event_jButton1ActionPerformed

    private void activate(AxisChart2DComponent chartComponent, int index) {
        displayStackImpl.add(chartComponent);
        chartComponent.attachTo(axisChart);
        ((CheckList.Element) defaultListModel1.getElementAt(index)).isAvtive = true;

    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int[] indices = jList2.getSelectedIndices();
        for (int index : indices) {
            AxisChart2DComponent chartComponent = listInstalledChartComponent.get(index);
            activate(chartComponent, index);
        }
        jList2.repaint();
        axisChart.repaintChartSurface();
}//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        int index = jList2.getSelectedIndex();
        if (index == -1) {
            return;
        }
        AxisChart2DComponent component = listInstalledChartComponent.get(index);
        JFrame jFrame = component.getFrame();
        jFrame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        if (jFrame != null) {
            jFrame.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "This component doesn't have any configuration panel.");
        }
}//GEN-LAST:event_jButton4ActionPerformed
